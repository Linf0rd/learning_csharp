using System;

namespace exercises
{
  class Comments
  {
    static void Main(string[] args)
    {
      // This is a single line comment.
      Console.WriteLine("Single-line comments start with two forward slashes (//)");
      Console.WriteLine("// This is a single line comment");
      Console.WriteLine("");

      // Multi-line comments
      Console.WriteLine("Multi-line comments start with /* and ends with */");
      Console.WriteLine("/* This is a multi-line comment */");
    }
  }
}