// To print text or output values, you can use the "WriteLine()" method.

using System;

namespace exercises
{
  class Output
  {
    static void Main(string[] args)
    {
      // You can add as many WriteLine() methods as you want.
      Console.WriteLine("Hello World!");
      Console.WriteLine("I am Learning C#");
      Console.WriteLine("It is awesome!");

      // You can also output numbers and perform mathematical calculations
      Console.WriteLine(3 + 3);

      // There is also a Write() method, which is similar to WriteLine().
      Console.Write("Hello World! ");
      Console.Write("I will print on the same line.");
    }
  }
}